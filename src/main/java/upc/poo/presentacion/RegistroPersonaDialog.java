    private void aEntidad() {
        p = new Persona();

        NombreCompleto n = new NombreCompleto();
        n.setPrimerNombre(this.jTextField1.getText());
        n.setSegundoNombre(this.jTextField2.getText());
        n.setPrimerApellido(this.jTextField3.getText());
        n.setSegundoApellido(this.jTextField4.getText());

        Identificacion i = new Identificacion();
        i.setTipo(TipoDeIdentificacion.values()[this.jComboBox2.getSelectedIndex()]);
        i.setNumero(this.jTextField10.getText());

        Contacto c = new Contacto();
        c.setTelefonoFijo(this.jTextField6.getText());
        c.setTelefonoCelular(this.jTextField7.getText());
        c.setDireccion(this.jTextField8.getText());
        c.setCorreoElectronico(this.jTextField9.getText());

        p.setNombre(n);
        p.setIdentificacion(i);
        p.setContacto(c);
        p.setFechaNacimiento(LocalDate.now());
        p.setGenero(Genero.values()[this.jComboBox1.getSelectedIndex()]);
    }

